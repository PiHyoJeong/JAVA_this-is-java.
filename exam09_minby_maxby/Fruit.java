package sec05.exam09_minby_maxby;

public class Fruit {
	public String name;
	public int price;
	public Fruit(String name, int price) {
		super();
		this.name = name;
		this.price = price;
	}
	public String getName() {
		return name;
	}
	public int getPrice() {
		return price;
	}
	
	

}
